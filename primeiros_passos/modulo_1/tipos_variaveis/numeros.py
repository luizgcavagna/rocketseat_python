numero_inteiro = 0
numero_real = 1.14

print("Inteiro: ", numero_inteiro)
print("Real: ", numero_real)

# type()
print("Tipo do numero: ", type(numero_real))

# soma +
soma = 0 + 0
print("Soma: ", soma)

# subtracao -
subtracao = 0 - 0
print("Subtracao: ", subtracao)

# multiplicacao *
multiplicacao = 0 * 0
print("Multiplicacao: ", multiplicacao)

# divisao /
divisao = 0 / 1
print("Divisao: ", divisao)
# sempre retorna um float

# divisao inteiro //
divisao = 0 // 1
print("Divisao: ", divisao)
# com duas barras retorna inteiro

# modulo %
modulo = 2 % 2
print("Modulo: ", modulo)

# int()
print("int(): ", int(divisao))

# float()
print("float(): ", float(divisao))